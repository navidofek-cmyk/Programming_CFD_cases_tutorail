inline double airfoil_upper(double x) {
    const auto& T = RAE2822_TABLE;
    if (x <= T.front().x) return T.front().yu;
    if (x >= T.back().x)  return T.back().yu;
    for (int i = 0; i + 1 < NRAE; ++i) {
        if (x <= T[i+1].x) {
            double t = (x - T[i].x) / (T[i+1].x - T[i].x);
            return T[i].yu + t * (T[i+1].yu - T[i].yu);
        }
    }
    return 0.0;
}

inline double airfoil_lower(double x) {
    const auto& T = RAE2822_TABLE;
    if (x <= T.front().x) return T.front().yl;
    if (x >= T.back().x)  return T.back().yl;
    for (int i = 0; i + 1 < NRAE; ++i) {
        if (x <= T[i+1].x) {
            double t = (x - T[i].x) / (T[i+1].x - T[i].x);
            return T[i].yl + t * (T[i+1].yl - T[i].yl);
        }
    }
    return 0.0;
}
